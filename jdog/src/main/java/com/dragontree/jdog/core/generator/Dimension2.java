package com.dragontree.jdog.core.generator;

public class Dimension2 {
    private final int width;
    private final int height;

    public Dimension2(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }
}
